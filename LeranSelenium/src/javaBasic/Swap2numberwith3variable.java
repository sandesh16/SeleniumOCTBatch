package javaBasic;

public class Swap2numberwith3variable {

	public static void main(String[] args) {
		
		int a = 5;
		int b = 10;
		int temp = 0;

		temp = a; // --> temp =5
		a = b; // --> a=10
		b = temp; // --> b=5
		
		System.out.println("value of a is:-" +a);
		System.out.println("value of b is:-" +b);

		
	
	}

}
